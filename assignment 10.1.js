/*
Are Higher Order functions and Call back functions the same ? If not,
briefly explain about both functions.
*/

/*
 No, they are different.
 Call back function: Function that is passed inside another function 
                    as an argument and is executed inside that another function is called 
                    a call back function. As the name suggests, call back functions
                    can be called from another function.

Higher order function: Function that returns another function is 
                        called a higher order function. 
*/